let fruits =["Apple","Orange","Grapes",89];
console.log(fruits);
console.log(fruits[0]);
console.log(fruits[1]);
console.log(fruits[2]);
console.log(fruits[3]);
console.log(fruits.length);

// to  add as last element 
fruits.push("Pineapple");
console.log(fruits);

// to add as 1st element 
fruits.unshift("Jamun");
console.log(fruits);

//  to delete the last element
fruits.pop();
console.log(fruits)

// to delete the 1st element 
fruits.shift();
console.log(fruits);
